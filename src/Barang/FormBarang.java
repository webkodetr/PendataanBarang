//    untuk menghapus data yang diinputan
    private void clear() {
        TAG = false;
        tfIDBARANG.setEnabled(true);
        tfIDBARANG.setText("");
        tfNAMABARANG.setText("");
        tfQTY.setText("");
        tfHARGA.setText("");

        read();
    }

//    menampilkan data pada tabel
    private void read() {
        bc.read(tblBARANG);

    }

    private boolean validasi() {
        if (tfIDBARANG.getText().isEmpty()
                || tfNAMABARANG.getText().isEmpty()
                || tfQTY.getText().isEmpty()
                || tfHARGA.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    private void save() {
        if (validasi()) {
            if (TAG) {
                create(); // true
            } else {
                update(); // false
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Inptan belum diisi!");
        }
    }

    private void create() {
        Barang b = new Barang();
        b.setID_BARANG(Integer.valueOf(tfIDBARANG.getText()));
        b.setNAMA_BARANG(tfNAMABARANG.getText());
        b.setQTY(Integer.valueOf(tfQTY.getText()));
        b.setHARGA(Integer.valueOf(tfHARGA.getText()));
        bc.create(b);

        clear();
    }

    private void update() {
        Barang b = new Barang();
        b.setID_BARANG(Integer.valueOf(tfIDBARANG.getText()));
        b.setNAMA_BARANG(tfNAMABARANG.getText());
        b.setQTY(Integer.valueOf(tfQTY.getText()));
        b.setHARGA(Integer.valueOf(tfHARGA.getText()));
        bc.update(b);

        clear();
    }

    private void loadData() {
        int select = tblBARANG.getSelectedRowCount();

        if (select == 0) {
            JOptionPane.showMessageDialog(rootPane, "Data Belum Dipilih!");
        } else {

            int pilih = JOptionPane.NO_OPTION;
            pilih = JOptionPane.showConfirmDialog(rootPane, "Yakin ingin Ubah!",
                    "Informasi", JOptionPane.YES_NO_OPTION, 2);

            if (pilih == JOptionPane.YES_OPTION) {
                int row = tblBARANG.getSelectedRow();
                tfIDBARANG.setText(tblBARANG.getValueAt(row, 0).toString());
                tfNAMABARANG.setText(tblBARANG.getValueAt(row, 1).toString());
                tfQTY.setText(tblBARANG.getValueAt(row, 2).toString());
                tfHARGA.setText(tblBARANG.getValueAt(row, 3).toString());

                tfIDBARANG.setEnabled(false);
                TAG = false;
            }
        }
    }

    private void delete() {
        int select = tblBARANG.getSelectedRowCount();

        if (select == 0) {
            JOptionPane.showMessageDialog(rootPane, "Data Belum Dipilih!");
        } else {

            int pilih = JOptionPane.NO_OPTION;
            pilih = JOptionPane.showConfirmDialog(rootPane, "Yakin ingin Hapus!",
                    "Informasi", JOptionPane.YES_NO_OPTION, 2);

            if (pilih == JOptionPane.YES_OPTION) {
                int row = tblBARANG.getSelectedRow();

                int id = Integer.valueOf(tblBARANG.getValueAt(row, 0).toString());
                bc.delete(id);

                clear();
            }
        }
    }

    private void search() {
        if (tfPENCARIAN.getText().isEmpty()) {
            clear();
        } else {
            bc.search(tblBARANG, Integer.valueOf(tfPENCARIAN.getText()));
        }
    }

